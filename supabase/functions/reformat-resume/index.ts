import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { resumeText, prompt, templateName } = await req.json();
    const openAIApiKey = Deno.env.get('OPENAI_API_KEY');
    
    if (!openAIApiKey) {
      throw new Error('OpenAI API key not configured');
    }

    console.log(`Reformatting resume using ${templateName} template`);

    const systemPrompt = `You are an expert resume formatter. Your task is to reformat the provided resume text according to the specific template requirements while preserving all factual information.

Rules:
1. Keep all dates, names, companies, and factual details exactly as provided
2. Improve formatting, structure, and presentation
3. Enhance language to be more professional and impactful
4. Add appropriate sections and headings
5. Use bullet points and proper formatting
6. Do not invent new information
7. Focus on the template style requested

Template-specific instructions: ${prompt}`;

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: `Please reformat this resume:\n\n${resumeText}` }
        ],
        temperature: 0.3,
        max_tokens: 2000,
      }),
    });

    if (!response.ok) {
      throw new Error(`OpenAI API error: ${response.status}`);
    }

    const data = await response.json();
    const reformattedText = data.choices[0].message.content;

    return new Response(JSON.stringify({
      reformattedText,
      templateName,
      success: true
    }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });

  } catch (error) {
    console.error('Error in reformat-resume function:', error);
    return new Response(JSON.stringify({ 
      error: 'Resume formatting failed', 
      details: error.message 
    }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});